## What Is 2FA (In Normal Person Language)

Two-factor authentication means you need two things to log in:
1. Something you know (your password)
2. Something you have (your phone)

Even if someone steals your password, they cannot get in without your phone.

Think of it like your house. A password is a key. 2FA is a key plus a fingerprint scanner. Even if someone copies your key, they still cannot get in.

The technical name is "multi-factor authentication" or MFA. Same thing. Two factors. Multiple factors. You need more than just a password.

### The Three Types of 2FA

**1. SMS Text Message (Weak but Better Than Nothing)**
- You get a code via text
- You type that code to log in
- Problem: Hackers can clone SIM cards or intercept texts
- Use this only if nothing else is available

**2. Authenticator App (Strong and Free)**
- App on your phone generates rotating 6-digit codes
- Codes change every 30 seconds
- Cannot be intercepted
- This is what you should use

**3. Security Key (Strongest but Costs Money)**
- Physical device you plug into your computer or tap on your phone
- Impossible to phish
- Costs $25-50
- Best for high-value accounts like banking

For most people, authenticator apps are the sweet spot. Free, secure, and work everywhere.

