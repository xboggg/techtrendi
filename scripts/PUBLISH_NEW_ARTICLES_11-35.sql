## Frequently Asked Questions

**Q: What''s the difference between 2FA and MFA?**

Nothing meaningful. 2FA means two factors. MFA means multiple factors (two or more). People use them interchangeably.

Technically MFA could mean three factors (password + phone + fingerprint), but in practice both terms mean "password plus something else."

