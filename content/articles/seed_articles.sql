## How to Actually Create Good Passwords

Here is my system for the rare occasions I need to create a password manually (like a master password for a password manager):

**The Passphrase Method:**

1. Think of four random, unrelated words
2. Make them vivid and weird (easier to remember)
3. Add a number and symbol somewhere
4. Aim for 20+ characters

Example thought process:
- Random words: purple, elephant, dancing, refrigerator
- Make it weird: elephants do not dance, refrigerators do not belong in this scene—perfect, it is memorable
- Add security: `Purple-Elephant-Dancing-7-Refrigerator!`

