## How Hackers Actually Get Your Passwords

Before we fix anything, you need to understand what you are protecting against. And it is probably not what you imagine.

**Forget the movie version.** Some hooded figure in a dark room typing furiously, "breaking through firewalls." That is fiction. Real password theft is boring and automated.

Here is what actually happens:

### Method 1: Data Breaches (Most Common)

Companies get hacked. Their user databases get stolen. Those databases contain emails and passwords—sometimes encrypted, sometimes not. Hackers then try those email/password combinations on other popular sites.

This is called **credential stuffing**, and it works because people reuse passwords.

Over 24 billion username/password combinations are currently available on the dark web. Twenty-four billion. If you have used the internet for more than a few years, yours are probably in there somewhere.

### Method 2: Phishing

You get an email that looks legitimate. "Your Netflix account has been suspended!" You click the link, enter your password on a fake site that looks exactly like Netflix. Now they have it.

Phishing has gotten scary sophisticated. I have seen fake login pages that were pixel-perfect copies of the real thing.

### Method 3: Brute Force

Automated tools try millions of password combinations until one works. This is why `password123` gets cracked in literally seconds, while `7$kLm@nQ9#xR` takes centuries.

### Method 4: Social Engineering

Someone calls pretending to be tech support. Or they guess your security questions based on your Facebook posts. (Your mother''s maiden name is not hard to find if you know where to look.)

