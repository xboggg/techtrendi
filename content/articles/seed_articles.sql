## The Three Rules of Password Security

Everything else I am about to tell you stems from three fundamental rules. Get these right, and you are ahead of 90% of internet users.

### Rule 1: Every Account Gets a Unique Password

I know. I KNOW. It sounds insane. How are you supposed to remember 150 different passwords?

You are not. That is what password managers are for. (We will get there.)

But this rule is non-negotiable. One password per account. Period.

Here is why: if one service gets breached—and services get breached constantly—hackers can only access that one account. With unique passwords, a LinkedIn breach does not also mean they can access your email, your bank, and your Amazon account.

### Rule 2: Length Beats Complexity

This might surprise you. `Tr0ub4dor&3` looks secure, right? Mixed case, numbers, symbols.

It is actually easier to crack than `correct horse battery staple`.

Why? Math.

A computer trying to crack `Tr0ub4dor&3` faces about 3 trillion possible combinations. Sounds like a lot until you realize modern computers can try billions per second.

`correct horse battery staple` (four random words) has about 10^28 possible combinations. That is... a lot more. Like, incomprehensibly more.

Long passwords—especially random word combinations—are exponentially harder to crack than short complex ones.

**Minimum recommendation:** 16 characters. Ideally 20+.

### Rule 3: Enable Two-Factor Authentication Everywhere

Even the best password can be compromised. 2FA means that even if someone has your password, they cannot get in without also having access to your phone or physical security key.

It is not optional anymore. Any important account without 2FA is a liability.

