## Two-Factor Authentication: Your Safety Net

Even with perfect passwords, enable 2FA on every account that offers it.

**Types of 2FA (ranked by security):**

1. **Hardware security keys** (YubiKey, etc.) - Most secure, requires physical device
2. **Authenticator apps** (Google Authenticator, Authy) - Very secure, generates codes on your phone
3. **SMS codes** - Better than nothing, but can be intercepted
4. **Email codes** - Weakest option, only use if nothing else available

**My recommendation:** Use an authenticator app as your default. Upgrade to hardware keys for critical accounts (email, banking) if you want maximum security.

**Setting up 2FA:**

Every site is slightly different, but generally:
1. Go to account settings → security
2. Look for "Two-Factor Authentication" or "2-Step Verification"
3. Choose your method and follow the prompts
4. Save your backup codes somewhere secure (not on your phone)

I wrote a complete [2FA setup guide](/blog/two-factor-authentication-guide) with step-by-step instructions for all major platforms.

