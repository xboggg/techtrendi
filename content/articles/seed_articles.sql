## What Makes a Good Password (And What Does Not)

Let me show you what hackers see when they look at common password patterns:

**Terrible passwords** (cracked in seconds):
- `password`
- `123456789`
- `qwerty`
- `letmein`
- `iloveyou`

**Bad passwords** (cracked in minutes to hours):
- `Sarah1990!` (name + birth year + symbol)
- `Cowboys#1` (favorite team)
- `Summer2024!` (season + year)
- `P@ssw0rd` (common substitutions are predictable)

**Mediocre passwords** (cracked in days to weeks):
- `MyD0g$Name123` (personal info + predictable pattern)
- `Tr0ub4dor&3` (short but complex)

**Strong passwords** (cracked in years to never):
- `correct-horse-battery-staple`
- `7kM$nQ9xR2pL@4vB`
- `purple-elephant-dancing-tuesday`

The pattern? Long and random beats short and complex every time.

