// Combined set for fast lookup during validation
let _validSet: Set<string> | null = null;

export function isValidWord(word: string): boolean {
  if (!_validSet) {
    _validSet = new Set([
      ...TARGET_WORDS.map(w => w.toLowerCase()),
      ...VALID_WORDS.map(w => w.toLowerCase()),
    ]);
  }
  return _validSet.has(word.toLowerCase());
}

export function getRandomTarget(): string {
  return TARGET_WORDS[Math.floor(Math.random() * TARGET_WORDS.length)].toUpperCase();
}

export function getDailyTarget(dateString?: string): string {
  const date = dateString || new Date().toISOString().split('T')[0];
  // Deterministic hash from date string
  let hash = 0;
  for (let i = 0; i < date.length; i++) {
    hash = ((hash << 5) - hash + date.charCodeAt(i)) | 0;
  }
  const index = Math.abs(hash) % TARGET_WORDS.length;
  return TARGET_WORDS[index].toUpperCase();
}
