// Pre-build a Set for O(1) lookup
export const WORD_SET = new Set(WORD_LIST.map(w => w.toLowerCase()));

// Pre-build a map of starting letter -> words for AI word selection
export const WORDS_BY_LETTER: Record<string, string[]> = {};
for (const word of WORD_LIST) {
  const letter = word[0].toLowerCase();
  if (!WORDS_BY_LETTER[letter]) {
    WORDS_BY_LETTER[letter] = [];
  }
  WORDS_BY_LETTER[letter].push(word.toLowerCase());
}

// Deduplicate each letter bucket
for (const letter of Object.keys(WORDS_BY_LETTER)) {
  WORDS_BY_LETTER[letter] = Array.from(new Set(WORDS_BY_LETTER[letter]));
}
